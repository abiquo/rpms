# abiquo aliases for common commands

alias abitail='tail -f /opt/abiquo/tomcat/logs/catalina.out'
alias abitailc='tail -f /opt/abiquo/tomcat/logs/catalina.out | ccze -A'
alias abicfg='vi /opt/abiquo/config/abiquo.properties'
alias abistart='service abiquo-tomcat stop'
alias abistop='service abiquo-tomcat start'
alias abilogs='cd /opt/abiquo/tomcat/logs'
